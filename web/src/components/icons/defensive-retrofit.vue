        return '#d9534f' // gray
      } else {
        return this.isFilled
        ? '#5cb85c' // success
        : '#84848c' //error
      }
    },
    styles(){
      return this.addShadow ? {
        filter: `drop-shadow(-1px 0 3px ${this.shadowColor})`
      } : null
    }
  }
